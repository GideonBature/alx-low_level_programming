#include "3-calc.h"
/**
 * op_add - add two numbers
 * @a: first num
 * @b: second num
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two numbers
 * @a: first num
 * @b: second num
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: first num
 * @b: second num
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: first num
 * @b: second num
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_add - add two numbers
 * @a: first num
 * @b: second num
 * Return: a + b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
