#include "3-calc.h"

/**
 * op_add - adds two nums
 * @a: first param
 * @b: second param
 * Return: sum
*/
int op_add(int a, int b)
{
    return (a + b);
}

/**
 * op_sub - subtracts two nums
 * @a: first param
 * @b: second param
 * Return: difference
*/
int op_sub(int a, int b)
{
    return (a - b);
}

/**
 * op_mul - multiplies two nums
 * @a: first param
 * @b: second param
 * Return: product
*/
int op_mul(int a, int b)
{
    return (a * b);
}

/**
 * op_div - divides two nums
 * @a: first param
 * @b: second param
 * Return: division
*/
int op_div(int a, int b)
{
    return (a / b);
}

/**
 * op_mod - finds modulos of two nums
 * @a: first param
 * @b: second param
 * Return: remainder
*/
int op_mod(int a, int b)
{
    return (a % b);
}