#include "main.h"
#include <stdio.h>

/**
 * add - adds a and b
 * @a: first para
 * @b: second para
 *
 * Return: an int
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts a and b
 * @a: first para
 * @b: second para
 *
 * Return: an int
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies a and b
 * @a: first para
 * @b: second para
 *
 * Return: an int
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides a and b
 * @a: first para
 * @b: second para
 *
 * Return: an int
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns reminder of a divide by b
 * @a: first para
 * @b: second para
 *
 * Return: an int
 */
int mod(int a, int b)
{
	return (a % b);
}
